package products;

public abstract class Product {
    // 이름, 가격
    private int id;
    private String name;
    private int price;

    public Product(int id, String name, int price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public int getId() {
        return id;
    }

    public abstract void applyOption();
    public abstract String getOptionToString();
}
